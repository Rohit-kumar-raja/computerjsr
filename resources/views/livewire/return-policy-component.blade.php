    <h1 class="sub-head">Faq</h1>

    <p class="para"><span class="sub-head1">Q. What is Open Box Delivery?</span></p>

    <p class="para">Ans: In Open Box Delivery, our Wishmaster will open the package in front of you at the time of delivery. Both outside as well as the brand packing is opened and shown to you. This ensures that you only receive what you ordered.
    </p>
<br><br>
    <p class="para"><span class="sub-head1">Q. Is this Service available everywhere?</span></p>

    <p class="para">Ans: Open Box Delivery will only be available for select pincodes in Bhopal, Bhubaneshwar and Chandigarh. If your order is eligible for OBD, you will get an option to select Open Box Delivery in the Order Summary page.
    </p>
    
<br><br>
<p class="para"><span class="sub-head1">Q. How do I avail of this service?</span></p>

<p class="para">Ans: In case you're eligible for Open Box Delivery, you'll get an option of Open Box Delivery at the Order Summary page. You can avail of the service by selecting the same option while ordering.
</p>

<br><br>
<p class="para"><span class="sub-head1">Q. Is this Service Free?</span></p>

<p class="para">Ans: Open Box Delivery is a Free service.
</p>

<br><br>
<p class="para"><span class="sub-head1">Q. In case of COD, when should I make payment? Before or After Open Box Delivery?</span></p>

<p class="para">Ans: You should always complete the payment before availing of Open box Delivery. In case you find the product is missing, damaged or completely different from what you ordered you can return the product immediately and get a refund on it.
</p>
<br><br>
<p class="para"><span class="sub-head1">Q. What will happen if the product is found to be missing/damaged/not what I ordered at the time of the delivery?</span></p>

<p class="para">Ans: In case you find the product to be missing, damaged or completely different from what you ordered, please ask the Wishmaster to take it back immediately. In case of COD orders, you'll get your refund immediately from the Wishmaster. In case of prepaid orders, you'll get a refund credited to your account according to the Seller's returns policy.
</p>    

<br><br>
<p class="para"><span class="sub-head1">Q. Can I get a replacement instead of refund?</span></p>

<p class="para">Ans: You can only get refund in case the product is deemed not acceptable.</p>


